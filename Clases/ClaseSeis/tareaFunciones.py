def sumar(x,y) : 
    suma = x + y 
    return suma
def restar(x,y) :
    resta = x - y
    return resta
def multiplicar(x,y) :
    multiplicacion = x * y
    return multiplicacion
def dividir(x,y) :
    division = x / y
    return division

print(sumar(2,2))
print(restar(2,23))
print(multiplicar(3,7))
print(dividir(25,5))